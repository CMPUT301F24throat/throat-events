package com.example.pickme.models.Enums;

/**
 * Enum representing the type of notifications in the system.
 */
public enum NotificationType {
    /**
     * Notification for selection.
     */
    SELECTION,

    /**
     * Notification for an announcement.
     */
    ANNOUNCEMENT,

    /**
     * Notification for rejection.
     */
    REJECTION
}